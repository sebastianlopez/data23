                    <article id="glosario">
                        <h2>3. GLOSARIO</h2>
                        <p>
                            <strong class="mini-titulo">Datos Personales:</strong> Cualquier información vinculada o que pueda asociarse a una o varias personas naturales determinadas o determinables.
                        </p>

                        <p>
                            <strong class="mini-titulo">Datos Personales Privados:</strong> Aquellos cuyo conocimiento es restringido al público.
                        </p>

                        <p>
                            <strong class="mini-titulo">Datos Sensibles:</strong> Son aquellos que afectan la intimidad del Titular o cuyo uso indebido puede generar su discriminación, tales como aquellos que revelen el origen racial o étnico, la orientación política, las convicciones religiosas o filosóficas, la pertenencia a sindicatos, organizaciones sociales, de derechos humanos o que promueva intereses de cualquier partido político o que garanticen los derechos y garantías de partidos políticos de oposición así como los datos relativos a la salud, a la vida sexual y los datos biométricos, entre otros, la captura de imagen fija o en movimiento, huellas digitales, fotografías, iris, reconocimiento de voz, facial o de palma de mano, etc.
                        </p>

                        <p>
                            <strong class="mini-titulo">Datos Públicos:</strong> Dato que no sea semiprivado, privado o sensible, que puede ser tratado por cualquier persona, sin necesidad de autorización para ello. Son públicos, entre otros, los datos contenidos en el registro civil de las personas (p.ej. si se es soltero o casado, hombre o mujer) y aquellos contenidos en documentos públicos (p.ej. contenidos en Escrituras Públicas), en registros públicos (p.ej. el registro de antecedentes disciplinarios de la Procuraduría), en gacetas y boletines oficiales y en sentencias judiciales ejecutoriadas que no estén sometidas a reserva.
                        </p>

                        <p>
                            <strong class="mini-titulo">Titular de la Información:</strong> Persona natural cuyos datos personales sean objeto de Tratamiento.
                        </p>

                        <p>
                            <strong class="mini-titulo">Causahabiente:</strong> Persona que ha sucedido a otra por causa del fallecimiento de ésta (heredero o legatario).
                        </p>

                        <p>
                            <strong class="mini-titulo">Base de Datos:</strong> Conjunto organizado de datos personales que sea objeto de Tratamiento (ej. Base de Datos de clientes, entre otras).
                        </p>

                        <p>
                            <strong class="mini-titulo">Tratamiento de Datos:</strong> Cualquier operación o conjunto de operaciones sobre datos personales, tales como la recolección, almacenamiento, uso, circulación o supresión.
                        </p>

                        <p>
                            <strong class="mini-titulo">Responsable del Tratamiento:</strong> Persona natural o jurídica, pública o privada, que por sí misma o en asocio con otros, decida sobre la base de datos y/o el Tratamiento de los datos.

                        </p>

                        <p>
                            <strong class="mini-titulo">Encargado del Tratamiento:</strong> Persona natural o jurídica, pública o privada, que por sí misma o en asocio con otros, realice el Tratamiento de datos personales por cuenta del Responsable del Tratamiento.
                        </p>

                        <p>
                            <strong class="mini-titulo">Autorización:</strong> Consentimiento previo, expreso e informado del Titular para llevar a cabo el Tratamiento de datos personales. El consentimiento puede otorgarse por escrito, de forma oral o mediante conductas inequívocas del Titular que permitan concluir que otorgó la autorización.
                        </p>

                        <p>
                            <strong class="mini-titulo">Aviso de Privacidad:</strong> Comunicación verbal o escrita cuyo fin es informar al titular de los datos sobre la existencia de un manual de políticas de tratamiento que le será aplicable al procesamiento de su información.
                        </p>

                        <p>
                            <strong class="mini-titulo">Transferencia:</strong> Envío de datos, dentro o fuera del territorio nacional, cuyo remitente y, a su vez, destinatario, es un Responsable del Tratamiento de Datos.
                        </p>

                        <p>
                            <strong class="mini-titulo">Transmisión:</strong> Comunicación de Datos, dentro o fuera del territorio colombiano, cuyo remitente es el Responsable y su receptor es el Encargado del Tratamiento de Datos
                        </p>

                        <p>
                            <strong class="mini-titulo">Oficial de Protección de Datos:</strong> Encargado de vigilar, controlar y promover la aplicación de la Política de Protección de Datos Personales al interior de La Compañía.
                        </p>
