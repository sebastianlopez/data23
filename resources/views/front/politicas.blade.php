                        <h2>7. DERECHOS Y DEBERES DEL TRATAMIENTO</h2>
                        <p>
                            7.1 <strong class="mini-titulo">Derechos de los Titulares.</strong> El Titular de los datos personales tiene los siguientes derechos:

                        <ol type="a" class="separar">
                            <li>
                                Conocer, actualizar y rectificar sus datos personales frente a los Responsables del Tratamiento.
                            </li>

                            <li>
                                Solicitar prueba de la autorización otorgada.
                            </li>

                            <li>
                                Ser informado, previa solicitud, respecto del uso que se les dará a sus datos personales.
                            </li>

                            <li>
                                Presentar ante la Superintendencia de Industria y Comercio (SIC) quejas por infracciones a lo dispuesto en las normas sobre datos personales.
                            </li>

                            <li>
                                Solicitar la supresión de los datos personales.
                            </li>

                            <li>
                                Revocar la autorización mediante la presentación de una solicitud y/o reclamo. Esta no procede cuando el Titular tenga un deber legal o contractual de permanecer en la base de datos.
                            </li>

                            <li>
                                Solicitar a la Superintendencia de Industria y Comercio (SIC) que ordene la revocatoria de la autorización y/o la supresión de los datos.
                            </li>

                            <li>
                                Consultar de forma gratuita sus datos personales, al menos una vez cada mes calendario y cada vez que existan modificaciones sustanciales de las políticas de Tratamiento de la información.
                            </li>
                        </ol>
                        </p>

                        <p>
                            <strong class="mini-titulo">Derechos de los niños, niñas y adolecentes.</strong> Si en algún momento dentro de la operación de la compañía hubiere lugar al tratamiento de la información personal de  niños, niñas y adolescentes esta se encontrará  sujeta a una especial protección. <br>

                            El tratamiento de este tipo especial de datos, requerirá el desarrollo y divulgación por parte de La Compañía de los términos y condiciones específicos de la respectiva actividad, definiendo entre otros, los requisitos, condiciones y restricciones para el tratamiento de información de niños, niñas y adolescentes, tomando en consideración en todo momento el interés superior y el respeto a los derechos prevalentes de los menores. Ante la ausencia de términos y condiciones específicos para el desarrollo de un determinado programa o actividad que involucre tratamiento de datos especiales de menores, aplicarán las disposiciones de la presente política y las normas especiales pertinentes. <br>

                            Siempre que sea necesario el tratamiento de los datos personales de niños, niñas y adolescentes, se tendrá en cuenta la opinión del menor conforme a la razonable determinación de su nivel de madurez y entendimiento del caso específico, lo que se entenderá surtido para todos los efectos legales, con el otorgamiento de la autorización del tratamiento por parte del representante legal.
                        </p>

                        <p>
