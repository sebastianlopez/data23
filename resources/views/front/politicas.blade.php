                        <h2>5. PRINCIPIOS GENERALES, POSTULADOS y PRINCIPIOS ESPECÍFICOS</h2>

                        <h4>5.1 PRINCIPIOS GENERALES Y POSTULADOS.</h4>
                        <p>
                            La Compañía garantiza la protección de derechos como el Habeas Data, la privacidad, la intimidad, el buen nombre, honra e imagen personal, con tal propósito, todas las actuaciones se regirán por los postulados de la buena fe, la legalidad, la autodeterminación informática, la libertad y la transparencia. <br>

                            Quien en ejercicio de su actividad suministre cualquier tipo de información o dato personal a la Compañía en su condición de encargado o responsable del tratamiento, podrá ejercer sus derechos como titular de la información para conocerla, actualizarla y rectificarla conforme a los procedimientos establecidos en la ley aplicable y la presente política. <br>

                            La Compañía reconoce que su legítimo derecho al tratamiento de los datos personales de los titulares de información, debe ser ejercido dentro del marco específico de la legalidad y del consentimiento del titular, procurando en todo momento preservar el equilibrio entre los derechos y deberes de titulares, responsables y encargados del tratamiento vinculados a su operación <br>

                        </p>

                        <h4>5.2. PRINCIPIOS ESPECÍFICOS</h4>
                        <p>
                            La Compañía aplicará los siguientes principios específicos que se establecen a continuación, los cuales constituyen las reglas a seguir en la recolección, manejo, uso, tratamiento, almacenamiento, intercambio y supresión de datos personales:
                        </p>

                        <ol type="a" class="separar">
                            <li>
                                <span class="resaltar">Principio de legalidad:</span> En el uso, captura, recolección y tratamiento de datos personales, se dará aplicación a las disposiciones vigentes y aplicables que rigen el tratamiento de datos personales y demás derechos fundamentales conexos.
                            </li>

                            <li>
                                <span class="resaltar">Principio de libertad:</span> El uso, captura, recolección y tratamiento de datos personales sólo puede llevarse a cabo con el consentimiento, previo, expreso e informado del Titular. Los datos personales no podrán ser obtenidos o divulgados sin previa autorización, o en ausencia de mandato legal, estatutario, o judicial que releve el consentimiento.
                            </li>

                            <li>
                                <span class="resaltar">Principio de finalidad:</span> El uso, captura, recolección y tratamiento de datos personales a los que tenga acceso y sean acopiados y recogidos en el desarrollo de las actividades de la Compañía, estarán subordinados y atenderán una finalidad legítima, la cual debe serle informada al respectivo titular de los datos personales.
                            </li>

                            <li>
                                <span class="resaltar">Principio de veracidad o calidad:</span> La información sujeta a uso, captura, recolección y tratamiento de datos personales debe ser veraz, completa, exacta, actualizada, comprobable y comprensible. Se prohíbe el Tratamiento de datos parciales, incompletos, fraccionados o que induzcan a error.
                            </li>

                            <li>
                                <span class="resaltar">Principio de transparencia:</span> En el uso, captura, recolección y tratamiento de datos personales debe garantizarse el derecho del Titular a obtener de la Compañía, en cualquier momento y sin restricciones, información acerca de la existencia de cualquier tipo de información o dato personal que sea de su interés o titularidad.

                            </li>

                            <li>
                                <span class="resaltar">Principio de acceso y circulación restringida:</span> Los datos personales, salvo la información pública, no podrán estar disponibles en Internet u otros medios de divulgación o comunicación masiva, salvo que el acceso sea técnicamente controlable para brindar un conocimiento restringido sólo a los Titulares o terceros autorizados. Para estos propósitos la obligación de La Compañía, será de medio.
                            </li>

                            <li>
                                <span class="resaltar">Principio de seguridad:</span> Los datos personales e información usada, capturada, recolectada y sujeta a tratamiento en el desarrollo de las actividades de la Compañía, serán objeto de protección en la medida en que los recursos técnicos y estándares mínimos así lo permitan, a través de la adopción de medidas tecnológicas de protección, protocolos, y todo tipo de medidas administrativas que sean necesarias para otorgar seguridad a los registros y repositorios físico y electrónicos, evitando su adulteración, modificación, pérdida, consulta, y en general en contra de cualquier uso o acceso no autorizado.
                            </li>

                            <li>
                                <span class="resaltar">Principio de confidencialidad:</span> Todas y cada una de las personas que administran, manejen, actualicen o tengan acceso a información de la naturaleza personal, se comprometen a conservar y mantener de manera estrictamente confidencial y no revelar a terceros, la información personal, comercial, contable, técnica, o de cualquier otro tipo suministrada en la ejecución y ejercicio de sus funciones. Este deber se hace extensivo a todos aquellos terceros aliados, colaboradores o vinculados que se relacionen de mediante cualquier vínculo convencional o contractual con la Compañía.
                            </li>

                            <li>
                                <span class="resaltar">Incorporación sistemática:</span> Los principios de Protección de Datos Personal se implementarán e irradiarán la interpretación de todos los procesos y procedimientos de la Compañía.
                            </li>

                        </ol>
